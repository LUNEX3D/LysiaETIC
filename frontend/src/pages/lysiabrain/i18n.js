export const brainTranslations = { tr, en };

/**
 * Hook: useBrainT  returns a translation function for LysiaBrain
 * Falls back to TR if key not found in current language
 */
/**
 * getBrainT  Memoized translation function factory
 * Ayn language iin her zaman ayn fonksiyon referansn dndrr.
 * Bu sayede React useCallback/useMemo dependency'lerinde gereksiz
 * re-render tetiklenmez.
 */
const _tCache = {};
export const getBrainT = (language) => {
    const lang = language || "tr";
    if (_tCache[lang]) return _tCache[lang];
    const dict = brainTranslations[lang] || brainTranslations.tr;
    const fn = (key) => dict[key] || brainTranslations.tr[key] || key;
    _tCache[lang] = fn;
    return fn;
};
